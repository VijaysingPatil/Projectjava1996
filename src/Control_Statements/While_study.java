package Control_Statements;

public class While_study {

	public static void main(String[] args) 
	{

		int a = 3;
		while(a<=30) 
		{
			System.out.println(a);
			a=a+3;
		}
		System.out.println("==================================");
		int b = 30 ;
		while (b>=3) 
		{
			System.out.println(b);
			b= b-3;
		}
		System.out.println("=============================");
		int c=4;
		while (c<=40)
		{
			System.out.println(c);
			c=c+4;
		}
			
		}
	}


