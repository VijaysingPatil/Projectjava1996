package Control_Statements;

public class Switch_study {

	public static void main(String[] args)
	//if year is 1-->Welcome to 1 year Engineering
	//if year is 2-->Welcome to 2 year Engineering
	//if year is 3-->Welcome to 3 year Engineering
	//if year is 4-->Welcome to 4 year Engineering
	
	
	{
		int Year = 1;
		switch (Year) 
		
		
		{
		case 1:System.out.println("Welocome to 1 year Engineering");
			
		break;
		
		case 2:System.out.println("Welocome to 2 year Engineering");
		break;
		
		case 3:System.out.println("Welocome to 3 year Engineering");
		break;
		
		case 4:System.out.println("Welocome to 4 year Engineering");
		break;
		 
		default:System.out.println("plz enter valid year between 1-4");
		
		
		
		}
		
		
		
		
	}

}
